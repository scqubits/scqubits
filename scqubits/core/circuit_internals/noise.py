    def _generate_overall_t1_quasiparticle_tunneling(self):
        """Generate the overall ``t1_quasiparticle_tunneling`` method.

        T1 Coherence Time Calculator due to Quasiparticle Tunneling
        ------------------------------------------------------------

        This function generates an overall method for calculating the T1
        coherence time due to quasiparticle tunneling for the entire circuit.

        Returns
        -------
        None

        Description
        -----------
        1. Checks if there are any existing methods for calculating T1 due to
           quasiparticle tunneling for any of the branches in the circuit. If
           not, it returns None.

        2. Checks if the circuit is purely harmonic, in which case it also
           returns None.

        3. If the checks pass, it defines a new method
           ``t1_quasiparticle_tunneling`` that calculates the overall T1 time
           due to quasiparticle tunneling.

        4. This method iterates over all the branches in the circuit that are
           Josephson junctions (indicated by ``"JJ"`` in the branch type), and
           for each branch, it calls the corresponding branch-specific T1
           calculation method.

        5. It then calculates the total rate of decoherence as the sum of the
           reciprocals of the T1 times for all the branches, and returns the
           reciprocal of the total rate (or the total rate itself if
           ``get_rate`` is True).

        6. Finally, the function adds the ``t1_quasiparticle_tunneling``
           method as an attribute of the current instance.

        Notes
        -----
        This function does not return anything; it modifies the current
        instance by adding the T1 calculation methods as attributes.
        """
        if not any(
            [
                re.match(r"t1_quasiparticle_tunneling\d+$", method)
                for method in dir(self)
            ]
        ):
            return None
        if self.is_purely_harmonic:
            return None

        def t1_quasiparticle_tunneling(
            self=self,
            i: int = 1,
            j: int = 0,
            Y_qp: float | Callable | None = None,
            x_qp: float = NOISE_PARAMS["x_qp"],
            T: float = NOISE_PARAMS["T"],
            Delta: float = NOISE_PARAMS["Delta"],
            total: bool = True,
            esys: tuple[ndarray, ndarray] | None = None,
            get_rate: bool = False,
        ) -> float:
            r"""Overall T1 time due to quasiparticle tunneling across all the Josephson junctions in a circuit.

            References: Smith et al (2020), Catelani et al (2011), Pop et al (2014).

            Parameters
            ----------
            i: int >=0
                state index that along with j defines a transition (i->j)
            j: int >=0
                state index that along with i defines a transition (i->j)
            Y_qp:
                complex admittance; a fixed value or function of `omega`
            x_qp:
                quasiparticle density (in units of eV)
            T:
                temperature in Kelvin
            Delta:
                superconducting gap (in units of eV)
            total:
                if False return a time/rate associated with a transition from state i to state j.
                if True return a time/rate associated with both i to j and j to i transitions
            esys:
                evals, evecs tuple
            get_rate:
                get rate or time

            Returns
            -------
            time or rate: float
                decoherence time in units of :math:`2\pi ({\rm system\,\,units})`, or rate in inverse units.
            """
            t1_times = []
            for branch in [b for b in self.branches if "JJ" in b.type]:
                t1_times.append(
                    getattr(self, f"t1_quasiparticle_tunneling{branch.index}")(
                        i=i,
                        j=j,
                        Y_qp=Y_qp,
                        x_qp=x_qp,
                        T=T,
                        Delta=Delta,
                        total=total,
                        esys=esys,
                    )
                )
            total_rate = sum([1 / t1 for t1 in t1_times])
            if get_rate:
                return total_rate
            return 1 / total_rate if total_rate != 0 else np.inf

        setattr(
            self,
            "t1_quasiparticle_tunneling",
            MethodType(t1_quasiparticle_tunneling, self),
        )

