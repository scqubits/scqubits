    def generate_noise_methods(self):
        """Dynamically generate all noise-calculation methods on the instance.

        This function is responsible for dynamically generating all the methods
        that calculate the different types of noise in the quantum circuit.

        Returns
        -------
        None

        Notes
        -----
        This function performs the following steps:

        1. Unfreezes the circuit to allow changes.
        2. Generates methods for calculating the derivatives of the Hamiltonian
           with respect to the different circuit parameters.
        3. Generates methods for calculating the Tphi times due to 1/f noise
           for each branch in the circuit.
        4. Generates methods for calculating the T1 times due to flux bias
           line noise for each branch in the circuit.
        5. Generates methods for calculating the T1 times due to various noise
           sources for each branch in the circuit.
        6. Generates methods for calculating the overall Tphi times due to
           charge coupling, flux, and ng noise for the entire circuit.
        7. Generates methods for calculating the overall T1 times due to
           capacitive, charge impedance, inductive, flux bias line, and
           quasiparticle tunneling noise for the entire circuit.
        8. Sets the ``_noise_methods_generated`` attribute to True.
        9. Freezes the circuit again to prevent further changes.

        The methods generated by this function are:

        - ``generate_methods_d_hamiltonian_d``
        - ``generate_tphi_1_over_f_methods``
        - ``generate_t1_flux_bias_line_methods``
        - ``generate_t1_methods``
        - ``generate_overall_tphi_cc``
        - ``generate_overall_tphi_flux``
        - ``generate_overall_tphi_ng``
        - ``generate_overall_t1_capacitive``
        - ``generate_overall_t1_charge_impedance``
        - ``generate_overall_t1_inductive``
        - ``generate_overall_t1_flux_bias_line``
        - ``generate_overall_t1_quasiparticle_tunneling``

        This function does not return anything; it modifies the current
        instance by adding the noise calculation methods as attributes.
        """
        self._frozen = False
        # Reset the noise-channel registry on each call so a re-generation
        # (e.g. after Circuit.configure(generate_noise_methods=True)) starts
        # from a clean slate. ``_register_noise_method`` populates it as
        # each per-channel method is bound.
        self._noise_channels_registry: dict[str, Callable] = {}
        self._generate_methods_d_hamiltonian_d()
        self._generate_tphi_1_over_f_methods()
        self._generate_t1_flux_bias_line_methods()
        self._generate_t1_methods()

        self._generate_overall_tphi_cc()
        self._generate_overall_tphi_flux()
        self._generate_overall_tphi_ng()
        self._generate_overall_t1_capacitive()
        self._generate_overall_t1_charge_impedance()
        self._generate_overall_t1_inductive()
        self._generate_overall_t1_flux_bias_line()
        self._generate_overall_t1_quasiparticle_tunneling()
        self._noise_methods_generated = True
        self._frozen = True

    def _register_noise_method(self, name: str, method: Callable) -> None:
        """Bind ``method`` on the instance as ``name`` and record it in the
        noise-channels registry.

        Used by every ``_generate_*`` helper instead of a bare ``setattr``
        so the registry stays in sync with what is actually bound on the
        instance. The registry is read by :meth:`channels` and via
        ``Circuit.supported_noise_channels``.

        Lazy-inits ``_noise_channels_registry`` so a per-channel
        generator can be invoked directly (e.g. in a unit test) without
        having to call :meth:`generate_noise_methods` first.

        Parameters
        ----------
        name:
            attribute name to bind on the instance.
        method:
            already-bound method object (typically the result of
            ``types.MethodType(func, self)``).
        """
        if not hasattr(self, "_noise_channels_registry"):
            self._noise_channels_registry = {}
        setattr(self, name, method)
        self._noise_channels_registry[name] = method

    def channels(self) -> dict[str, Callable]:
        """Return a copy of the noise-channel registry.

        The returned dict maps each generated noise-method name (e.g.
        ``"t1_capacitive"``, ``"t1_inductive3"``, ``"tphi_1_over_f_flux1"``)
        to the bound method object. Use this for static-tooling-friendly
        introspection of the noise surface, in preference to walking
        ``self.__dict__`` and pattern-matching on names.

        Returns
        -------
        Mapping from noise-channel name to bound callable. Empty if
        :meth:`generate_noise_methods` has not run yet.
        """
        registry = getattr(self, "_noise_channels_registry", None)
        if registry is None:
            return {}
        return dict(registry)
