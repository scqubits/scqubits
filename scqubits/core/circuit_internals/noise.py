class NoisyCircuit(NoisySystem, ABC):
    """Noisy-quantum-circuit mixin extending :class:`NoisySystem`.

    Provides a suite of methods for generating and evaluating expressions
    related to the Hamiltonian and its derivatives, as well as calculating
    the 1/f dephasing time or rate due to various noise sources.

    Attributes
    ----------
    noise_helper_methods:
        dictionary storing the noise helper methods generated by
        :meth:`_generate_methods_d_hamiltonian_d`

    Notes
    -----
    This class is designed to be subclassed and extended with specific noise
    models for different types of quantum circuits.
    """

    # class-level annotations for attributes provided by subclasses (see circuit_internals.routines)
    transformation_matrix: Any
    symbolic_circuit: Any
    is_grounded: bool
    return_parent_circuit: Callable[..., Any]

    @staticmethod
    def Q_from_branch(branch: Branch):
        """Extract the quality factor ``Q`` from a given branch.

        The key looked up in ``branch.aux_params`` is ``Q_ind`` for inductive
        branches (``branch.type == "L"``) and ``Q_cap`` otherwise. If the key
        is absent, ``None`` is returned. If the stored value is a numeric
        string, it is returned as a float. Otherwise the value is treated as
        a Python expression in ``omega`` and ``T`` and wrapped into a callable
        ``Q_func(omega, T)`` evaluated via :func:`eval`.

        Parameters
        ----------
        branch:
            branch from which to extract the quality factor; must carry a
            ``type`` attribute (``"L"`` or ``"C"``) and an ``aux_params``
            dictionary

        Returns
        -------
        Quality factor as a float, or a callable ``Q_func(omega, T)``
        returning the quality factor for given frequency and temperature.
        Returns ``None`` if the branch does not carry a quality factor.
        """
        key = "Q_" + ("ind" if branch.type == "L" else "cap")
        if key in branch.aux_params:
            Q_str = branch.aux_params[key]
            if not is_string_float(Q_str):  # type: ignore[arg-type]

                def Q_func(omega, T):
                    return eval(Q_str)

                return Q_func
            else:
                return float(Q_str)
        return None

    def _d_hamiltonian_d_function_factory(self, param_sym: sm.Symbol) -> Callable:
        """Build a method returning :math:`\\partial H / \\partial p`.

        The returned method differentiates the Hamiltonian symbolically with
        respect to the parameter ``param_sym`` and evaluates it numerically.

        Parameters
        ----------
        param_sym:
            symbolic parameter (an external flux or offset charge) with
            respect to which the symbolic Hamiltonian is differentiated

        Returns
        -------
        Callable suitable for binding to the instance, returning the operator
        :math:`\\partial H / \\partial p` for the chosen parameter ``p``.
        """

        def param_derivative(self, energy_esys: bool | tuple[ndarray, ndarray] = False):
            parent_instance = self.return_parent_circuit()
            hamiltonian = parent_instance._hamiltonian_sym_for_numerics
            hamiltonian = hamiltonian.subs("I", 1)
            all_sym_parameters = (
                list(parent_instance.symbolic_params.keys())
                + parent_instance.external_fluxes
                + parent_instance.offset_charges
            )
            diff_sym_expr = hamiltonian.diff(param_sym)
            diff_sym_expr = diff_sym_expr.expand()
            # substitute all symbolic params
            for param in all_sym_parameters:
                diff_sym_expr = diff_sym_expr.subs(
                    param, getattr(parent_instance, param.name)
                )
            # evaluate the expression
            native = parent_instance._evaluate_symbolic_expr(diff_sym_expr)
            return self.process_op(native_op=native, energy_esys=energy_esys)

        return param_derivative

    def _generate_methods_d_hamiltonian_d(self):
        """Generate ``d_hamiltonian_d_*`` helper methods on the instance.

        Generates methods returning the Hamiltonian derivative with respect to
        offset charges, external fluxes, and junction energies.

        For each external flux and offset charge, a method
        ``d_hamiltonian_d_flux<n>`` or ``d_hamiltonian_d_ng<n>`` is built via
        :meth:`_d_hamiltonian_d_function_factory` and bound to ``self``. For
        each Josephson-junction branch, a method ``d_hamiltonian_d_EJ<index>``
        is built that returns the negative of
        :meth:`_junction_related_evaluation` with ``calc='dhdEJ'``. All of
        these methods are also collected into the
        :attr:`noise_helper_methods` dictionary.

        Notes
        -----
        Mutates ``self`` by setting :attr:`noise_helper_methods` and binding
        the generated methods as instance attributes via :func:`setattr`.
        """

        ext_flux_1_over_f_methods = {}
        ng_1_over_f_methods = {}
        cc_1_over_f_methods = {}

        for param_sym in self.external_fluxes + self.offset_charges:

            param_derivative = self._d_hamiltonian_d_function_factory(param_sym)
            if param_sym in self.external_fluxes:
                ext_flux_1_over_f_methods[
                    f"d_hamiltonian_d_flux{get_trailing_number(param_sym.name)}"
                ] = param_derivative
            elif param_sym in self.offset_charges:
                ng_1_over_f_methods[
                    f"d_hamiltonian_d_ng{get_trailing_number(param_sym.name)}"
                ] = param_derivative
        ## cc noise methods
        junction_branches = [branch for branch in self.branches if "JJ" in branch.type]
        for idx, branch in enumerate(junction_branches):

            def param_derivative(self, branch=branch):
                return -self._junction_related_evaluation(branch, calc="dhdEJ")

            cc_1_over_f_methods[f"d_hamiltonian_d_EJ{branch.index}"] = param_derivative
        noise_helper_methods = {
            **ext_flux_1_over_f_methods,
            **ng_1_over_f_methods,
            **cc_1_over_f_methods,
        }
        self.noise_helper_methods = noise_helper_methods
        for method_name in noise_helper_methods:
            setattr(
                self, method_name, MethodType(noise_helper_methods[method_name], self)
            )

    def _transform_expr_to_new_variables(
        self, expr_node_vars: sm.Expr, substitute_symbol: str | None = None
    ):
        """Transform a symbolic expression from node-flux to new variables.

        Rewrites the input expression in the new (transformed) variables.

        The input expression in the original node variables ``φ<n>`` is
        rewritten in the new variables ``θ<n>`` using the instance's
        transformation matrix. When ``substitute_symbol`` is given, each free
        symbol is renamed to ``<substitute_symbol><trailing_number>``.

        Parameters
        ----------
        expr_node_vars:
            sympy expression in the old node variables representing a physical
            quantity in the circuit (e.g. Hamiltonian, current, voltage)
        substitute_symbol:
            optional name prefix for the new variables; when provided, each
            free symbol is renamed accordingly while preserving its trailing
            integer index

        Returns
        -------
        Transformed sympy expression in the new variables.
        """
        transformation_mat = self.transformation_matrix
        expr_node_vars = expr_node_vars.expand()
        num_vars = len(self.symbolic_circuit.nodes) - (1 if self.is_grounded else 0)
        new_vars = [sm.symbols(f"θ{index}") for index in range(1, 1 + num_vars)]
        old_vars = [sm.symbols(f"φ{index}") for index in range(1, 1 + num_vars)]
        transformed_expr = transformation_mat.dot(new_vars)
        for idx, var in enumerate(old_vars):
            expr_node_vars = expr_node_vars.subs(var, transformed_expr[idx])

        if substitute_symbol:
            for var in expr_node_vars.free_symbols:
                expr_node_vars = expr_node_vars.subs(
                    var,
                    sm.symbols(f"{substitute_symbol}{get_trailing_number(var.name)}"),
                )
        return expr_node_vars

    def _junction_related_evaluation(
        self, branch_junction: Branch, calc: str = "dhdEJ"
    ):
        """Evaluate a junction-related operator in the parent circuit basis.

        Locates, in the parent circuit's symbolic Hamiltonian, the term
        corresponding to the cosine of the phase difference across
        ``branch_junction`` (with external fluxes substituted back in after
        the matching step) and evaluates it numerically. With
        ``calc='sin_phi_qp'``, the cosine is converted to a sine of half the
        original argument before evaluation, yielding the operator used in
        quasiparticle-tunneling rates.

        Parameters
        ----------
        branch_junction:
            junction branch of the current instance's circuit
        calc:
            type of calculation; ``'dhdEJ'`` returns the Hamiltonian term
            associated with the junction, ``'sin_phi_qp'`` returns the
            half-angle sine operator used in quasiparticle calculations

        Returns
        -------
        Numerical evaluation of the requested junction operator.
        """
        parent_instance = self.return_parent_circuit()
        hamiltonian = parent_instance._hamiltonian_sym_for_numerics

        for sym in parent_instance.offset_charges + list(
            parent_instance.symbolic_params.keys()
        ):
            hamiltonian = hamiltonian.subs(sym, getattr(parent_instance, sym.name))
        hamiltonian = hamiltonian.subs("I", 1)
        branch_cos_node_expr = sm.cos(
            sm.symbols(f"φ{branch_junction.nodes[0].index}")
            - sm.symbols(f"φ{branch_junction.nodes[1].index}")
        )
        branch_cos_node_expr = branch_cos_node_expr.subs(
            "φ0", 0
        )  # setting ground node to zero.
        branch_cos_expr = parent_instance._transform_expr_to_new_variables(
            branch_cos_node_expr
        )
        expr_dict = hamiltonian.as_coefficients_dict()
        for term, coefficient in expr_dict.items():
            term_without_ext_flux = copy.copy(term)
            for flux in parent_instance.external_fluxes:
                term_without_ext_flux = term_without_ext_flux.subs(flux, 0)
            if term_without_ext_flux == branch_cos_expr:
                break
        # substitute external flux
        for flux in parent_instance.external_fluxes:
            term = term.subs(flux, getattr(parent_instance, flux.name))
        if calc == "sin_phi_qp":
            term = term.subs(sm.cos, sm.sin)
            term = term.subs(term.args[0], term.args[0] / 2)

        # evaluate the expression
        return parent_instance._evaluate_symbolic_expr(term)

    def _tphi_1_over_f_function_factory(self, noise_type: str, noise_op_func: Callable):
        """Build a method computing the 1/f dephasing time for a noise channel.

        Parameters
        ----------
        noise_type:
            noise-channel tag used to look up the default noise strength in
            ``NOISE_PARAMS`` (``'cc'``, ``'ng'``, or ``'flux'``)
        noise_op_func:
            callable returning the noise operator (typically a
            ``d_hamiltonian_d_*`` method generated by
            :meth:`_generate_methods_d_hamiltonian_d`)

        Returns
        -------
        Callable suitable for binding to the instance, computing the 1/f
        dephasing time (or rate) by delegating to :meth:`tphi_1_over_f`.
        """

        def tphi_1_over_f_func(
            self=self,
            A_noise: float = NOISE_PARAMS[f"A_{noise_type}"],
            i: int = 0,
            j: int = 1,
            esys: tuple[ndarray, ndarray] | None = None,
            get_rate: bool = False,
            **kwargs,
        ) -> float:
            """This function calculates the 1/f dephasing time (or rate) due to a
            specific type of noise in the quantum circuit. Refer to the name of the method to determine the type of noise.

            Parameters
            ----------
                A_noise:
                    The strength of the noise. This is a scaling factor that determines the magnitude of the noise.
                i:
                    The state index that, along with `j`, defines a qubit. This is an integer that identifies one of the states of the qubit.
                j:
                    The state index that, along with `i`, defines a qubit. This is an integer that identifies one of the states of the qubit.
                esys:
                    A tuple containing the eigenvalues and eigenvectors of the system Hamiltonian. This is used to calculate the transition frequencies between the states of the qubit.
                get_rate:
                    A flag that determines whether to calculate the dephasing time or the dephasing rate. If this is True, the function calculates the dephasing rate; otherwise, it calculates the dephasing time.

            Returns
            -------
                float: The calculated 1/f dephasing time (or rate). This is a numerical value that represents the time it takes for the qubit to lose phase coherence due to the noise, or the rate at which the qubit loses phase coherence due to the noise.
            """
            noise_op = noise_op_func()
            if isinstance(noise_op, qt.Qobj):
                noise_op = Qobj_to_scipy_csc_matrix(noise_op)

            if isinstance(noise_op, list):
                noise_op_new = []
                for op in noise_op:
                    if isinstance(op, qt.Qobj):
                        op = Qobj_to_scipy_csc_matrix(op)
                    noise_op_new.append(op)
                noise_op = noise_op_new

            return self.tphi_1_over_f(
                A_noise=A_noise,
                i=i,
                j=j,
                noise_op=noise_op,
                esys=esys,
                get_rate=get_rate,
                **kwargs,
            )

        return tphi_1_over_f_func

    def _generate_tphi_1_over_f_methods(self):
        """Generate ``tphi_1_over_f_*`` dephasing methods on the instance.

        This function is a dynamic method generator, crafting methods for
        calculating the 1/f dephasing time (or rate) due to different types of
        noise in the quantum circuit. The generated methods are named
        ``tphi_1_over_f_{noise_type}{index}``, where ``noise_type`` can be
        ``'cc'``, ``'ng'``, or ``'flux'``, and ``index`` differentiates
        individual noise sources.

        external_fluxes :
            A collection of symbols representing external fluxes in the circuit.
        offset_charges :
            A collection of symbols representing offset charges in the circuit.
        branches :
            A collection of branches in the circuit.

        Notes
        -----
        1. Identifies the branches in the circuit that are junctions (indicated
           by ``"JJ"`` in the branch type).
        2. Initializes dictionaries to store the generated methods for each
           type of noise.
        3. Iterates over the external fluxes, offset charges, and junction
           branches in the circuit.
        4. Depending on the type of parameter (external flux, offset charge, or
           junction branch), it determines the type of noise and the function
           to differentiate the Hamiltonian with respect to the parameter.
        5. If the parameter is an expression, it extracts the trailing number
           from the parameter name and uses it to get the appropriate
           differentiation function from the current instance.
        6. If the parameter is a junction branch, it uses the branch's ID
           string as the trailing number and gets the appropriate
           differentiation function from the current instance.
        7. Defines a function ``tphi_1_over_f_func`` that calculates the 1/f
           dephasing time (or rate) for the current parameter. This function
           invokes the differentiation function to generate the noise operator,
           converts the noise operator to a sparse matrix if necessary, and
           then calls the ``tphi_1_over_f`` method of the current instance to
           calculate the 1/f dephasing time (or rate).
        8. Adds the ``tphi_1_over_f_func`` function to the appropriate
           dictionary, depending on the type of parameter.
        9. Merges the dictionaries into a single dictionary ``noise_methods``.
        10. Iterates over the ``noise_methods`` dictionary and adds each method
            as an attribute of the current instance.

        This function does not return anything; it modifies the current instance
        by adding the 1/f dephasing time (or rate) calculation methods as
        attributes.
        """
        # calculating the rates from each of the flux sources
        junction_branches = [branch for branch in self.branches if "JJ" in branch.type]
        methods_noise_rates_from_flux = {}
        methods_noise_rates_from_ng = {}
        methods_noise_rates_from_cc = {}
        for param_sym in self.external_fluxes + self.offset_charges + junction_branches:
            if param_sym in self.external_fluxes:
                diff_func_name = "d_hamiltonian_d_flux"
                noise_type = "flux"
            elif param_sym in self.offset_charges:
                diff_func_name = "d_hamiltonian_d_ng"
                noise_type = "ng"
            if param_sym in junction_branches:
                diff_func_name = "d_hamiltonian_d_EJ"
                noise_type = "cc"
            if isinstance(param_sym, sm.Expr):
                trailing_number = get_trailing_number(param_sym.name)
                noise_op_func = getattr(self, f"{diff_func_name}{trailing_number}")
            elif param_sym in junction_branches:
                trailing_number = param_sym.index
                noise_op_func = getattr(self, f"{diff_func_name}{trailing_number}")

            tphi_1_over_f_func = self._tphi_1_over_f_function_factory(
                noise_type=noise_type, noise_op_func=noise_op_func
            )
            if param_sym in self.external_fluxes:
                methods_noise_rates_from_flux[
                    f"tphi_1_over_f_flux{trailing_number}"
                ] = tphi_1_over_f_func
            elif param_sym in self.offset_charges:
                methods_noise_rates_from_ng[f"tphi_1_over_f_ng{trailing_number}"] = (
                    tphi_1_over_f_func
                )
            elif param_sym in junction_branches:
                methods_noise_rates_from_cc[f"tphi_1_over_f_cc{trailing_number}"] = (
                    tphi_1_over_f_func
                )

        noise_methods = {
            **methods_noise_rates_from_flux,
            **methods_noise_rates_from_ng,
            **methods_noise_rates_from_cc,
        }
        for method_name in noise_methods:
            self._register_noise_method(method_name, MethodType(noise_methods[method_name], self))

    def _generate_overall_tphi_cc(self):
        """Generate the overall ``tphi_1_over_f_cc`` method on the instance.

        This function calculates the overall dephasing time (Tphi) due to 1/f
        critical current (cc) noise in the quantum circuit.

        Steps
        -----
        1. Checks if there are any existing methods for calculating Tphi due to
           1/f cc noise. This is done by searching the methods of the current
           instance for method names that match the pattern
           ``"tphi_1_over_f_cc\\d+$"``. If such methods exist, the function
           returns None and does not generate a new method.

        2. Defines a new method ``tphi_1_over_f_cc`` for calculating the
           overall Tphi due to 1/f cc noise. This method performs the following
           steps:

            a. Initializes an empty list ``tphi_times`` to store the Tphi
               times for each junction branch in the circuit.

            b. Iterates over the junction branches in the circuit. A junction
               branch is a branch that represents a Josephson junction (JJ).

            c. Calls the method for calculating Tphi due to 1/f cc noise for
               the current junction branch. The method is an attribute of the
               current instance that is named ``"tphi_1_over_f_cc"`` followed
               by the ID string of the branch.

            d. Appends the calculated Tphi time to the ``tphi_times`` list.

            e. Calculates the total rate of dephasing by summing the
               reciprocals of the Tphi times.

            f. If the ``get_rate`` parameter is True, returns the total rate
               of dephasing. Otherwise, returns the reciprocal of the total
               rate (the overall Tphi time), or infinity if the total rate is
               zero.

        3. Adds the ``tphi_1_over_f_cc`` method as an attribute of the current
           instance. This is done using the ``setattr`` function and the
           ``MethodType`` class to bind the method to the current instance.

        Notes
        -----
        This function does not return anything; it modifies the current
        instance by adding the ``tphi_1_over_f_cc`` method as an attribute.
        """
        if not any([re.match(r"tphi_1_over_f_cc\d+$", method) for method in dir(self)]):
            return None

        def tphi_1_over_f_cc(
            self=self,
            A_noise: float = NOISE_PARAMS["A_cc"],
            i: int = 0,
            j: int = 1,
            esys: tuple[ndarray, ndarray] | None = None,
            get_rate: bool = False,
            **kwargs,
        ) -> float:
            r"""Calculate the 1/f dephasing time (or rate) due to critical current noise of
            junction associated with Josephson energy in the circuit.

            Parameters
            ----------
            A_noise:
                noise strength
            i:
                state index that along with j defines a qubit
            j:
                state index that along with i defines a qubit
            esys:
                evals, evecs tuple
            get_rate:
                get rate or time

            Returns
            -------
                decoherence time in units of :math:`2\pi ({\rm system\,\,units})`, or rate in inverse units.
            """
            tphi_times = []
            for branch in [brnch for brnch in self.branches if "JJ" in brnch.type]:
                tphi_times.append(
                    getattr(self, f"tphi_1_over_f_cc{branch.index}")(
                        A_noise=A_noise,
                        i=i,
                        j=j,
                        esys=esys,
                        **kwargs,
                    )
                )
            total_rate = sum([1 / tphi for tphi in tphi_times])
            if get_rate:
                return total_rate
            return 1 / total_rate if total_rate != 0 else np.inf

        self._register_noise_method("tphi_1_over_f_cc", MethodType(tphi_1_over_f_cc, self))

    def _generate_overall_tphi_flux(self):
        """Generate the overall ``tphi_1_over_f_flux`` method on the instance.

        This function calculates the overall dephasing time (Tphi) due to 1/f
        flux noise in the quantum circuit.

        Steps
        -----
        1. Checks if there are any existing methods for calculating Tphi due
           to 1/f flux noise. This is done by searching the methods of the
           current instance for method names that match the pattern
           ``"tphi_1_over_f_flux\\d+$"``. If such methods exist, the function
           returns None and does not generate a new method.

        2. Defines a new method ``tphi_1_over_f_flux`` for calculating the
           overall Tphi due to 1/f flux noise. This method performs the
           following steps:

            a. Initializes an empty list ``tphi_times`` to store the Tphi
               times for each external flux in the circuit.

            b. Iterates over the external fluxes in the circuit.

            c. Calls the method for calculating Tphi due to 1/f flux noise for
               the current external flux. The method is an attribute of the
               current instance that is named ``"tphi_1_over_f_flux"`` followed
               by the trailing number in the name of the flux.

            d. Appends the calculated Tphi time to the ``tphi_times`` list.

            e. Calculates the total rate of dephasing by summing the
               reciprocals of the Tphi times.

            f. If the ``get_rate`` parameter is True, returns the total rate
               of dephasing. Otherwise, returns the reciprocal of the total
               rate (the overall Tphi time), or infinity if the total rate is
               zero.

        3. Adds the ``tphi_1_over_f_flux`` method as an attribute of the
           current instance. This is done using the ``setattr`` function and
           the ``MethodType`` class to bind the method to the current
           instance.

        Notes
        -----
        This function does not return anything; it modifies the current
        instance by adding the ``tphi_1_over_f_flux`` method as an attribute.
        """
        if not any(
            [re.match(r"tphi_1_over_f_flux\d+$", method) for method in dir(self)]
        ):
            return None

        def tphi_1_over_f_flux(
            self=self,
            A_noise: float = NOISE_PARAMS["A_flux"],
            i: int = 0,
            j: int = 1,
            esys: tuple[ndarray, ndarray] | None = None,
            get_rate: bool = False,
            **kwargs,
        ) -> float:
            r"""Calculate the 1/f dephasing time (or rate) due to flux noise.

            Parameters
            ----------
            A_noise:
                noise strength
            i:
                state index that along with j defines a qubit
            j:
                state index that along with i defines a qubit
            esys:
                evals, evecs tuple
            get_rate:
                get rate or time

            Returns
            -------
                decoherence time in units of :math:`2\pi ({\rm system\,\,units})`, or rate in inverse units.
            """
            tphi_times = []
            for flux_sym in self.external_fluxes:
                tphi_times.append(
                    getattr(
                        self, f"tphi_1_over_f_flux{get_trailing_number(flux_sym.name)}"
                    )(
                        A_noise=A_noise,
                        i=i,
                        j=j,
                        esys=esys,
                        **kwargs,
                    )
                )
            total_rate = sum([1 / tphi for tphi in tphi_times])
            if get_rate:
                return total_rate
            return 1 / total_rate if total_rate != 0 else np.inf

        self._register_noise_method("tphi_1_over_f_flux", MethodType(tphi_1_over_f_flux, self))

    def _generate_overall_tphi_ng(self):
        """Generate the overall ``tphi_1_over_f_ng`` method on the instance.

        Overall Dephasing Time (Tphi) Calculator due to 1/f Flux Noise
        --------------------------------------------------------------

        This function calculates the overall dephasing time (Tphi) due to 1/f
        flux noise in the quantum circuit.

        Steps
        -----
        1. Checks if there are any existing methods for calculating Tphi due
           to 1/f flux noise. This is done by searching the methods of the
           current instance for method names that match the pattern
           ``"tphi_1_over_f_flux\\d+$"``. If such methods exist, the function
           returns None and does not generate a new method.

        2. Defines a new method ``tphi_1_over_f_flux`` for calculating the
           overall Tphi due to 1/f flux noise. This method performs the
           following steps:

            a. Initializes an empty list ``tphi_times`` to store the Tphi
               times for each external flux in the circuit.

            b. Iterates over the external fluxes in the circuit.

            c. Calls the method for calculating Tphi due to 1/f flux noise for
               the current external flux. The method is an attribute of the
               current instance that is named ``"tphi_1_over_f_flux"`` followed
               by the trailing number in the name of the flux.

            d. Appends the calculated Tphi time to the ``tphi_times`` list.

            e. Calculates the total rate of dephasing by summing the
               reciprocals of the Tphi times.

            f. If the ``get_rate`` parameter is True, returns the total rate
               of dephasing. Otherwise, returns the reciprocal of the total
               rate (the overall Tphi time), or infinity if the total rate is
               zero.

        3. Adds the ``tphi_1_over_f_flux`` method as an attribute of the
           current instance. This is done using the ``setattr`` function and
           the ``MethodType`` class to bind the method to the current
           instance.

        Notes
        -----
        This function does not return anything; it modifies the current
        instance by adding the ``tphi_1_over_f_flux`` method as an attribute.
        """
        if not any([re.match(r"tphi_1_over_f_ng\d+$", method) for method in dir(self)]):
            return None

        def tphi_1_over_f_ng(
            self=self,
            A_noise: float = NOISE_PARAMS["A_ng"],
            i: int = 0,
            j: int = 1,
            esys: tuple[ndarray, ndarray] | None = None,
            get_rate: bool = False,
            **kwargs,
        ) -> float:
            r"""Calculate the 1/f dephasing time (or rate) due to charge noise.

            Parameters
            ----------
            A_noise:
                noise strength
            i: int >=0
                state index that along with j defines a qubit
            j: int >=0
                state index that along with i defines a qubit
            esys:
                evals, evecs tuple
            get_rate:
                get rate or time

            Returns
            -------
            time or rate: float
                decoherence time in units of :math:`2\pi ({\rm system\,\,units})`, or rate
                in inverse units.
            """
            tphi_times = []
            for flux_sym in self.offset_charges:
                tphi_times.append(
                    getattr(
                        self, f"tphi_1_over_f_ng{get_trailing_number(flux_sym.name)}"
                    )(
                        A_noise=A_noise,
                        i=i,
                        j=j,
                        esys=esys,
                        **kwargs,
                    )
                )
            total_rate = sum([1 / tphi for tphi in tphi_times])
            if get_rate:
                return total_rate
            return 1 / total_rate if total_rate != 0 else np.inf

        self._register_noise_method("tphi_1_over_f_ng", MethodType(tphi_1_over_f_ng, self))

